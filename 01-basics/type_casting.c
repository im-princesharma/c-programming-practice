// Header file for input and output functions
#include <stdio.h>

// Main function
int main()
{
    // Variable declaration
    int a = 10;
    int b = 3;

    // Type casting integer values to float
    float result = (float)a / b;

    // Display result
    printf("Result = %.2f\n", result);

    // Return 0 indicates successful execution
    return 0;
}