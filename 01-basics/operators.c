// Header file for input and output functions
#include <stdio.h>

// Main function
int main()
{
    int a = 10;
    int b = 5;

    // Arithmetic operators
    printf("Addition = %d\n", a + b);
    printf("Subtraction = %d\n", a - b);
    printf("Multiplication = %d\n", a * b);
    printf("Division = %d\n", a / b);

    // Program executed successfully
    return 0;
}