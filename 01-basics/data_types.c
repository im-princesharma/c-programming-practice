// Header file for input and output functions
#include <stdio.h>

// Main function - program execution starts here
int main()
{
    // Integer data type
    int age = 19;

    // Float data type
    float marks = 89.5;

    // Character data type
    char grade = 'A';

    // Display values
    printf("Age = %d\n", age);
    printf("Marks = %.1f\n", marks);
    printf("Grade = %c\n", grade);

    // Return 0 indicates successful execution
    return 0;
}